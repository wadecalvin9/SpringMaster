package com.wadecalvin.spring.Models;

public class Photos {

    private  String id;
    private  String name;

    public Photos(){

    }

    public Photos(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
